#!/usr/bin/env python3
"""
a function def cat_arrays(arr1, arr2): that concatenates two arrays
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays into a new list.

    Parameters:
    - arr1: First list of integers or floats.
    - arr2: Second list of integers or floats.

    Returns:
    - A new list containing all elements of arr1
    followed by all elements of arr2.
    """
    return arr1 + arr2
